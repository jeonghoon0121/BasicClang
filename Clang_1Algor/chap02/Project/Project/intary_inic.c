/*배열의 요소값을 초기화하며 배열선언
* 배열의 요소에 넣을 값을 미리 알고있으면 선언ㄹ할때 초기화(initializer 할수 있다. 
* 1.코드 정리 20250718
* 
* 
*/
/*자료형이 int형이고 요소개수 5개인 배열*/
#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#define N 5 /*배열의 요소개수*/
int main(void)
{
	int i;
	int a[N];	//배열선언
	for (i = 0; i < N; i++)/*각 요소에 값 입력*/
	{
		print("a[%d]", i);
		scanf("%d:,&a[i]");
	}
	puts("각 요소의 값");
	for (i = 0; i < N; i++)/**/
	{
		printf("a[%d]=%d\n", i, a[i]);
	}
	return 0;
}

